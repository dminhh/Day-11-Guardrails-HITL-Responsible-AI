    "    \"\"\"Returns (is_injection, matched_pattern).\"\"\"\n"
    "    for p in INJECTION_PATTERNS:\n"
    "        if re.search(p, text, re.IGNORECASE):\n"
    "            return True, p\n"
    "    return False, ''\n\n\n"
    "def topic_filter(text: str) -> bool:\n"
    "    \"\"\"Returns True if input should be BLOCKED.\"\"\"\n"
    "    lower = text.lower()\n"
    "    for t in BLOCKED_TOPICS:\n"
    "        if t in lower:\n"
    "            return True\n"
    "    for t in ALLOWED_TOPICS:\n"
    "        if t in lower:\n"
    "            return False\n"
    "    return True\n\n\n"
    "class InputGuardrailPlugin(base_plugin.BasePlugin):\n"
    "    \"\"\"\n"
    "    Blocks prompt injection and off-topic input before the LLM.\n"
    "    Why needed: Cheapest safety layer (no API call). Catches injections\n"
    "    that output guardrails miss (no PII output = nothing to redact).\n"
    "    \"\"\"\n\n"
    "    def __init__(self):\n"
    "        super().__init__(name='input_guardrail')\n"
