cells.append(md("## Layer 1: Rate Limiter\n\nPrevents abuse with sliding-window per-user rate limiting. Catches DoS and automated injection scripts that content-based layers cannot detect."))

cells.append(code(
    "class RateLimitPlugin(base_plugin.BasePlugin):\n"
    "    \"\"\"\n"
    "    Sliding-window rate limiter per user.\n"
    "    Why needed: Blocks automated attack scripts — other layers check content,\n"
    "    not request frequency. This is the only layer that can catch volume attacks.\n"
    "    \"\"\"\n\n"
    "    def __init__(self, max_requests: int = 10, window_seconds: int = 60):\n"
    "        super().__init__(name='rate_limiter')\n"
    "        self.max_requests = max_requests\n"
    "        self.window_seconds = window_seconds\n"
    "        self.user_windows: dict = defaultdict(deque)\n"
    "        self.blocked_count = 0\n"
    "        self.total_count = 0\n\n"
    "    def _get_user_id(self, ctx) -> str:\n"
    "        if ctx and hasattr(ctx, 'user_id'):\n"
    "            return ctx.user_id\n"
    "        return 'anonymous'\n\n"
    "    async def on_user_message_callback(\n"
    "        self, *, invocation_context: InvocationContext, user_message: types.Content\n"
    "    ) -> types.Content | None:\n"
    "        self.total_count += 1\n"
    "        user_id = self._get_user_id(invocation_context)\n"
    "        now = time.time()\n"
    "        window = self.user_windows[user_id]\n\n"
    "        while window and now - window[0] > self.window_seconds:\n"
    "            window.popleft()\n\n"
    "        if len(window) >= self.max_requests:\n"
    "            wait_time = int(self.window_seconds - (now - window[0])) + 1\n"
    "            self.blocked_count += 1\n"
    "            return types.Content(\n"
    "                role='model',\n"
    "                parts=[types.Part.from_text(\n"
    "                    text=f'Rate limit exceeded. You have sent {self.max_requests} requests '\n"
    "                         f'in {self.window_seconds}s. Please wait {wait_time}s.'\n"
    "                )]\n"
    "            )\n"
    "        window.append(now)\n"
    "        return None\n\n\n"
    "print('RateLimitPlugin ready!')"
))

